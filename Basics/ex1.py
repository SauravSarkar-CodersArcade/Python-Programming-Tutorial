name = 'Gaurav'
age = 20

print('My name is ' + name + ' my age is ' + str(age))
# 1. f - formatter
print(f'My name is {name} and my age is {age}')
# 2. format()
print('My name is {2} and my age is {3}'.format(name, age, "Sachin", 32))
