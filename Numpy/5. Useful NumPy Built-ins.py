import numpy as np

# Generate array of zeros
zeros = np.zeros((2, 3))

"""
Creates a 2x3 array of zeros:
[[0. 0. 0.]
 [0. 0. 0.]]
"""

# Generate array of ones
ones = np.ones((3, 2))

# Create range with step
ranged = np.arange(1, 10, 2)

# Generate 3x3 identity matrix
identity = np.eye(3)

print("Zeros:\n", zeros)
print("Ones:\n", ones)
print("Range:\n", ranged)
print("Identity Matrix:\n", identity)
